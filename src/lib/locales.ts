export const getTranslation = (lang: LanguageType, key: string, replaces?: Record<string, string | number>): string => {
  let text = translations[lang]?.[key] || key;
  if (replaces) {
    Object.entries(replaces).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v));
    });
  }
  return text;
};
